function MarqueeRow({ tiles }: { tiles: string[] }) {
  // Duplicate inline so the track is exactly 2x — translating by -50% loops seamlessly.
  const doubled = [...tiles, ...tiles]
  return (
    <div className="m-marquee-row relative w-full overflow-hidden">
      <div className="m-marquee-track">
        {doubled.map((src, i) => (
          <div
            key={`${src}-${i}`}
            className="relative shrink-0 mx-2 overflow-hidden p-px"
            style={{
              width: 280,
              height: 280,
              borderRadius: '14px',
              border: '1px solid var(--m-border-soft)',
              background: 'var(--m-surface-alt)',
            }}
            aria-hidden={i >= tiles.length}
          >
            <div className="relative w-full h-full overflow-hidden" style={{ borderRadius: 13 }}>
              <Image
                src={src}
                alt=""
                fill
                sizes="280px"
                className="object-contain p-6"
              />
            </div>
          </div>
        ))}
      </div>
    </div>
  )
}

